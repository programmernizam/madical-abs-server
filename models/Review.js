const mongoose = require("mongoose");

// Review Schema
const reviewSchema = mongoose.Schema(
  {
    name: {
      type: String,
      require: true,
    },
    email: {
      type: String,
      require: true,
    },
    city: {
      type: String,
      require: true,
    },
    image: {
      type: String,
      require: true,
    },
    description: {
      type: String,
      require: true,
    },
  },
  {
    timestamps: true,
  }
);

// Review Model
const Review = mongoose.model("Review", reviewSchema);
module.exports = Review;
